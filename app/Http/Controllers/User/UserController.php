        $alumnos=Alumnos::all();
        foreach ($alumnos as $alu) {
          if($alu->user_id == $user->id){
            $alum=$alu;
          }
        }

        $rol=roluser::all();
        return view('User.index')->with('user',$user)->with('rol',$rol)->with('alum',$alum);
